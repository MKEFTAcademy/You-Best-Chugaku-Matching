// カテゴリー日本語マップ
const categoryMap = {
    'all': 'すべて',
    'entrance': '入試情報',
    'school': '学校ニュース',
    'study': '学習・教育'
};

// カテゴリー色マップ
const categoryColorMap = {
    'entrance': '#f59e0b',
    'school': '#10b981',
    'study': '#8b5cf6'
};

// 現在の状態
let currentFilter = 'all';
let displayedCount = 6;
const itemsPerLoad = 6;

// 初期化
document.addEventListener('DOMContentLoaded', () => {
    initializeApp();
});

function initializeApp() {
    // フィルターボタンのイベントリスナー
    const filterButtons = document.querySelectorAll('.filter-btn');
    filterButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            filterButtons.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            currentFilter = btn.dataset.category;
            displayedCount = 6;
            displayNews();
        });
    });

    // 検索機能のイベントリスナー
    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.addEventListener('input', handleSearch);
        searchInput.addEventListener('focus', function() {
            this.style.borderColor = '#2563eb';
        });
        searchInput.addEventListener('blur', function() {
            this.style.borderColor = '#e5e7eb';
        });
    }

    // 「さらに読み込む」ボタンのイベントリスナー
    const loadMoreBtn = document.getElementById('load-more-btn');
    loadMoreBtn.addEventListener('click', () => {
        displayedCount += itemsPerLoad;
        displayNews();
    });

    // ニュースを表示
    displayNews();
}

function displayNews() {
    const newsContainer = document.getElementById('news-container');
    const loadMoreBtn = document.getElementById('load-more-btn');
    
    // フィルタリング（カテゴリー）
    let filteredNews = currentFilter === 'all' 
        ? newsData 
        : newsData.filter(news => news.category === currentFilter);
    
    // フィルタリング（検索キーワード）
    if (searchKeyword) {
        filteredNews = filteredNews.filter(filterNewsBySearch);
    }

    // 日付順でソート（新しい順）
    filteredNews.sort((a, b) => new Date(b.date) - new Date(a.date));

    // 表示するニュースを取得
    const newsToDisplay = filteredNews.slice(0, displayedCount);
    
    // コンテナをクリア
    newsContainer.innerHTML = '';

    if (newsToDisplay.length === 0) {
        const message = searchKeyword 
            ? `「${searchKeyword}」に該当するニュースが見つかりませんでした。`
            : '該当するニュースが見つかりませんでした。';
        newsContainer.innerHTML = `<div class="no-results">${message}</div>`;
        loadMoreBtn.style.display = 'none';
        return;
    }

    // ニュースカードを生成
    newsToDisplay.forEach(news => {
        const card = createNewsCard(news);
        newsContainer.appendChild(card);
    });

    // 「さらに読み込む」ボタンの表示/非表示
    if (displayedCount >= filteredNews.length) {
        loadMoreBtn.style.display = 'none';
    } else {
        loadMoreBtn.style.display = 'block';
    }
}

function createNewsCard(news) {
    const card = document.createElement('div');
    card.className = 'news-card';
    card.dataset.category = news.category;

    const categoryColor = categoryColorMap[news.category] || '#6b7280';
    const categoryName = categoryMap[news.category] || news.category;
    const formattedDate = formatDate(news.date);

    // Google検索URLを生成（タイトル + ソース名で検索）
    const searchQuery = encodeURIComponent(`${news.title} ${news.source}`);
    const searchUrl = `https://www.google.com/search?q=${searchQuery}`;

    card.innerHTML = `
        <div class="news-card-header">
            <div class="news-meta">
                <span class="news-date">${formattedDate}</span>
            </div>
            <span class="news-category" style="background-color: ${categoryColor};">${categoryName}</span>
            <h3 class="news-title">${escapeHtml(news.title)}</h3>
        </div>
        <div class="news-card-body">
            <p class="news-summary">${escapeHtml(news.summary)}</p>
        </div>
        <div class="news-card-footer">
            <a href="${searchUrl}" target="_blank" rel="noopener noreferrer" class="news-link">
                🔍 関連記事を検索
            </a>
        </div>
    `;

    return card;
}

function formatDate(dateString) {
    const date = new Date(dateString);
    const year = date.getFullYear();
    const month = date.getMonth() + 1;
    const day = date.getDate();
    
    const today = new Date();
    const yesterday = new Date(today);
    yesterday.setDate(yesterday.getDate() - 1);

    if (date.toDateString() === today.toDateString()) {
        return '今日';
    } else if (date.toDateString() === yesterday.toDateString()) {
        return '昨日';
    } else {
        return `${year}年${month}月${day}日`;
    }
}

function escapeHtml(text) {
    const map = {
        '&': '&amp;',
        '<': '&lt;',
        '>': '&gt;',
        '"': '&quot;',
        "'": '&#039;'
    };
    return text.replace(/[&<>"']/g, m => map[m]);
}

// 検索機能
let searchKeyword = '';

function handleSearch(e) {
    searchKeyword = e.target.value.toLowerCase().trim();
    displayedCount = 6;
    displayNews();
}

function filterNewsBySearch(news) {
    if (!searchKeyword) return true;
    
    // タイトル、要約、カテゴリー、ソースで検索
    const searchText = [
        news.title,
        news.summary,
        categoryMap[news.category] || '',
        news.source
    ].join(' ').toLowerCase();
    
    return searchText.includes(searchKeyword);
}
